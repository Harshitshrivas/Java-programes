public class chartoint {
    public static void main(String[] args) {
        char ch = 'A';
        int aa = ch;

        System.out.println("Character: " + ch);
        System.out.println("Integer: " + aa);

        int x = 66;
        char y = (char) x;

        System.out.println("Integer: " + x);
        System.out.println("Character: " + y);

    }
}
